function setLanguage(lang) {
    localStorage.setItem('language', lang);
    document.documentElement.lang = lang === 'en' ? 'en' : 'zh-Hant';
}

function getLanguage() {
    return localStorage.getItem('language') || 'zh-TW';
}

function getText(key, replacements = {}) {
    const lang = getLanguage();
    const langDict = translations[lang] || translations['zh-TW'];
    let text = langDict[key] || key;
    
    for (const placeholder in replacements) {
        text = text.replace(`{${placeholder}}`, replacements[placeholder]);
    }
    return text;
}

function applyTranslations() {
    const lang = getLanguage();
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = getText(key);
        el.textContent = text;
    });

    // Placeholder support (input/textarea) via data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const text = getText(key);
        if ('placeholder' in el) el.placeholder = text;
    });
}
